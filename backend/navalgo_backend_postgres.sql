-- =========================================================
-- 5) Legacy compatibility views (read-only)
--    Keep old naming model available for queries/reporting.
-- =========================================================

CREATE OR REPLACE VIEW usuarios AS
SELECT
    w.id,
    w.full_name AS nombre,
    w.email,
    w.password_hash AS password,
    w.speciality AS especialidad,
    CASE WHEN w.active THEN 'ACTIVO' ELSE 'INACTIVO' END AS estado,
    NULL::timestamp without time zone AS fecha_creacion
FROM workers w;

CREATE OR REPLACE VIEW clientes AS
SELECT
    o.id,
    CASE WHEN o.type = 'COMPANY' THEN 'EMPRESA' ELSE 'PARTICULAR' END AS tipo_cliente,
    o.document_id AS documento_identidad,
    o.display_name AS nombre_principal,
    NULL::varchar(100) AS persona_contacto,
    o.phone AS telefono,
    o.email,
    NULL::timestamp without time zone AS fecha_creacion
FROM owners o;

CREATE OR REPLACE VIEW barcos AS
SELECT
    v.id,
    v.owner_id AS cliente_id,
    v.name AS nombre,
    v.registration_number AS matricula,
    v.length_meters::numeric(5,2) AS eslora,
    v.model AS tipo,
    NULL::timestamp without time zone AS fecha_registro
FROM vessels v;

CREATE OR REPLACE VIEW partes_trabajo AS
SELECT
    wo.id,
    wo.vessel_id AS barco_id,
    wo.title AS titulo,
    wo.description AS descripcion,
    (
      SELECT COUNT(*)::integer
      FROM engine_hour_logs e
      WHERE e.work_order_id = wo.id
    ) AS numero_motores,
    CASE wo.status
      WHEN 'NEW' THEN 'PENDIENTE'
      WHEN 'IN_PROGRESS' THEN 'EN_CURSO'
      WHEN 'DONE' THEN 'FINALIZADO'
      WHEN 'CANCELLED' THEN 'CANCELADO'
      ELSE wo.status
    END AS estado,
    wo.priority AS prioridad,
    wo.created_at AS fecha_creacion,
    NULL::timestamp without time zone AS fecha_finalizacion
FROM work_orders wo;

CREATE OR REPLACE VIEW parte_asignaciones AS
SELECT
    wow.work_order_id AS parte_id,
    wow.worker_id AS usuario_id
FROM work_order_workers wow;

CREATE OR REPLACE VIEW motores_parte AS
SELECT
    e.id,
    e.work_order_id AS parte_id,
    e.engine_label AS identificador_motor,
    e.hours::numeric(10,2) AS horas_uso
FROM engine_hour_logs e;

CREATE OR REPLACE VIEW adjuntos_parte AS
SELECT
    a.id,
    a.work_order_id AS parte_id,
    a.file_url AS clave_objeto,
    a.file_type AS tipo_archivo,
    NULL::varchar(255) AS nombre_original,
    NULL::timestamp without time zone AS fecha_subida
FROM work_order_attachments a;

CREATE OR REPLACE VIEW ausencias AS
SELECT
    l.id,
    l.worker_id AS usuario_id,
    l.start_date AS fecha_inicio,
    l.end_date AS fecha_fin,
    CASE l.status
      WHEN 'PENDING' THEN 'PENDIENTE'
      WHEN 'APPROVED' THEN 'APROBADA'
      WHEN 'REJECTED' THEN 'RECHAZADA'
      ELSE l.status
    END AS estado,
    l.reason AS descripcion,
    NULL::timestamp without time zone AS fecha_solicitud,
    'GENERAL'::varchar(50) AS tipo_ausencia
FROM leave_requests l;

CREATE OR REPLACE VIEW fichajes AS
SELECT
    t.id,
    t.worker_id AS usuario_id,
    'JORNADA'::varchar(20) AS tipo,
    t.clock_in AS fecha_hora,
    NULL::numeric(9,6) AS latitud,
    NULL::numeric(9,6) AS longitud,
    NULL::text AS notas
FROM time_entries t;

COMMIT;
